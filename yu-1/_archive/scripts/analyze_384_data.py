def analyze_data():
    """384효 데이터 종합 분석"""

    print("=" * 70)
    print("    384효 점수 데이터 종합 분석")
    print("=" * 70)

    all_scores = [v[0] for v in YAO_DATA.values()]

    # 1. 기본 통계
    print("\n[1. 기본 통계]")
    print("-" * 50)
    print(f"  총 효사 수: {len(all_scores)}")
    print(f"  평균 점수: {sum(all_scores)/len(all_scores):.1f}")
    print(f"  최고 점수: {max(all_scores):.1f}")
    print(f"  최저 점수: {min(all_scores):.1f}")
    print(f"  점수 범위: {max(all_scores) - min(all_scores):.1f}")

    # 2. 점수 분포
    print("\n[2. 점수 분포 (길흉 분류)]")
    print("-" * 50)

    categories = {
        "대길 (85+)": 0,
        "길 (70-84)": 0,
        "평 (50-69)": 0,
        "흉 (30-49)": 0,
        "대흉 (<30)": 0
    }

    for score in all_scores:
        if score >= 85:
            categories["대길 (85+)"] += 1
        elif score >= 70:
            categories["길 (70-84)"] += 1
        elif score >= 50:
            categories["평 (50-69)"] += 1
        elif score >= 30:
            categories["흉 (30-49)"] += 1
        else:
            categories["대흉 (<30)"] += 1

    for cat, count in categories.items():
        pct = (count / len(all_scores)) * 100
        bar = "█" * int(pct / 2)
        print(f"  {cat}: {count:>3}개 ({pct:>5.1f}%) {bar}")

    # 3. 효 위치별 평균 점수
    print("\n[3. 효 위치별 평균 점수]")
    print("-" * 50)

    yao_position_scores = {i: [] for i in range(1, 7)}
    for (hex_num, yao_pos), scores in YAO_DATA.items():
        yao_position_scores[yao_pos].append(scores[0])

    for pos in range(1, 7):
        avg = sum(yao_position_scores[pos]) / len(yao_position_scores[pos])
        yao_names = {1: "초효", 2: "이효", 3: "삼효", 4: "사효", 5: "오효", 6: "상효"}
        bar = "█" * int(avg / 5)
        print(f"  {yao_names[pos]}: {avg:>5.1f} {bar}")

    # 4. 최고/최저 효사 TOP 10
    print("\n[4. 최고 점수 효사 TOP 10]")
    print("-" * 50)

    sorted_yao = sorted(YAO_DATA.items(), key=lambda x: x[1][0], reverse=True)

    for i, ((hex_num, yao_pos), scores) in enumerate(sorted_yao[:10], 1):
        yao_names = {1: "초효", 2: "이효", 3: "삼효", 4: "사효", 5: "오효", 6: "상효"}
        print(f"  {i:>2}. 제{hex_num}괘 {yao_names[yao_pos]}: {scores[0]:.1f}점")

    print("\n[5. 최저 점수 효사 TOP 10]")
    print("-" * 50)

    for i, ((hex_num, yao_pos), scores) in enumerate(sorted_yao[-10:], 1):
        yao_names = {1: "초효", 2: "이효", 3: "삼효", 4: "사효", 5: "오효", 6: "상효"}
        print(f"  {i:>2}. 제{hex_num}괘 {yao_names[yao_pos]}: {scores[0]:.1f}점")

    # 5. 괘별 평균 점수 분석
    print("\n[6. 괘 평균 점수 TOP 10 / BOTTOM 10]")
    print("-" * 50)

    sorted_hex = sorted(HEXAGRAM_AVERAGES.items(), key=lambda x: x[1], reverse=True)

    print("  상위 10개 괘:")
    for hex_num, avg in sorted_hex[:10]:
        bar = "█" * int(avg / 5)
        print(f"    제{hex_num:>2}괘: {avg:>5.1f} {bar}")

    print("\n  하위 10개 괘:")
    for hex_num, avg in sorted_hex[-10:]:
        bar = "█" * int(avg / 5)
        print(f"    제{hex_num:>2}괘: {avg:>5.1f} {bar}")

    # 6. 카테고리별 점수 분석
    print("\n[7. 카테고리별 평균 점수]")
    print("-" * 50)

    health_scores = [v[1] for v in YAO_DATA.values()]
    exam_scores = [v[2] for v in YAO_DATA.values()]
    wealth_scores = [v[3] for v in YAO_DATA.values()]
    love_scores = [v[4] for v in YAO_DATA.values()]

    print(f"  건강(健康): {sum(health_scores)/len(health_scores):.1f}")
    print(f"  시험(試驗): {sum(exam_scores)/len(exam_scores):.1f}")
    print(f"  재물(財物): {sum(wealth_scores)/len(wealth_scores):.1f}")
    print(f"  이성(異性): {sum(love_scores)/len(love_scores):.1f}")

