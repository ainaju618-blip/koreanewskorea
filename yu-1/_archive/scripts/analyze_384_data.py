    print(f"  평(50-69) 확률: {categories['평 (50-69)'] / 384 * 100:.1f}%")
    print(f"  흉(<50) 확률: {(categories['흉 (30-49)'] + categories['대흉 (<30)']) / 384 * 100:.1f}%")

    # 8. 통계적 특성
    print("\n[9. 통계적 특성]")
    print("-" * 50)

    import statistics
    std_dev = statistics.stdev(all_scores)
    median = statistics.median(all_scores)

    print(f"  중앙값 (Median): {median:.1f}")
    print(f"  표준편차 (Std Dev): {std_dev:.1f}")

