    print(f"  길:   {avg_expected:.0f}점 이상")
    print(f"  평:   {avg_expected - std_dev:.0f}점 ~ {avg_expected:.0f}점")
    print(f"  흉:   {avg_expected - std_dev:.0f}점 미만")


